from typing import List, Dict, Optional
from databricks.sdk import WorkspaceClient
from databricks.vector_search.client import VectorSearchClient
import json

class VectorStore:
    """Wrapper for Databricks Vector Search operations."""

    def __init__(
        self,
        endpoint_name: str,
        index_name: str,
        catalog: str,
        schema: str,
        chunks_table: str
    ):
        self.endpoint_name = endpoint_name
        self.index_name = index_name
        self.catalog = catalog
        self.schema = schema
        self.chunks_table = chunks_table

        self.workspace_client = WorkspaceClient()
        self.vs_client = VectorSearchClient()

    def create_endpoint(self, endpoint_type: str = "STORAGE_OPTIMIZED"):
        """Create Vector Search endpoint."""
        try:
            endpoint = self.workspace_client.vector_search_endpoints.create_endpoint(
                name=self.endpoint_name,
                endpoint_type=endpoint_type
            )
            print(f"Endpoint '{self.endpoint_name}' created successfully!")
            return endpoint
        except Exception as e:
            if "already exists" in str(e).lower():
                print(f"Endpoint '{self.endpoint_name}' already exists.")
            else:
                raise e

    def create_delta_sync_index(
        self,
        primary_key: str = "chunk_id",
        text_column: str = "text",
        embedding_model: str = "databricks-gte-large-en",
        pipeline_type: str = "TRIGGERED"
    ):
        """Create Delta Sync index with managed embeddings."""

        source_table = f"{self.catalog}.{self.schema}.{self.chunks_table}"

        try:
            index = self.workspace_client.vector_search_indexes.create_index(
                name=self.index_name,
                endpoint_name=self.endpoint_name,
                primary_key=primary_key,
                index_type="DELTA_SYNC",
                delta_sync_index_spec={
                    "source_table": source_table,
                    "embedding_source_columns": [
                        {
                            "name": text_column,
                            "embedding_model_endpoint_name": embedding_model
                        }
                    ],
                    "pipeline_type": pipeline_type
                }
            )
            print(f"Index '{self.index_name}' created successfully!")
            return index
        except Exception as e:
            if "already exists" in str(e).lower():
                print(f"Index '{self.index_name}' already exists.")
            else:
                raise e

    def sync_index(self):
        """Manually trigger index sync for TRIGGERED pipeline."""
        self.workspace_client.vector_search_indexes.sync_index(
            index_name=self.index_name
        )
        print(f"Index sync triggered for '{self.index_name}'.")

    def query(
        self,
        query_text: str,
        num_results: int = 50,
        filters: Optional[Dict[str, any]] = None,
        columns: Optional[List[str]] = None,
        query_type: str = "HYBRID"  # or "ANN"
    ) -> List[Dict[str, any]]:
        """Query the vector index.

        Args:
            query_text: Natural language query
            num_results: Number of results to return
            filters: Filter conditions (e.g., {"domain": "ERP", "module": "Order"})
            columns: Columns to return
            query_type: "HYBRID" (semantic + keyword) or "ANN" (semantic only)

        Returns:
            List of matching documents with scores
        """
        # For Storage-Optimized endpoints, use VectorSearchClient
        index = self.vs_client.get_index(
            endpoint_name=self.endpoint_name,
            index_name=self.index_name
        )

        # Build SQL-like filter string for Storage-Optimized
        filter_str = None
        if filters:
            conditions = []
            for key, value in filters.items():
                if isinstance(value, str):
                    conditions.append(f"{key} = '{value}'")
                elif isinstance(value, list):
                    values_str = ", ".join([f"'{v}'" for v in value])
                    conditions.append(f"{key} IN ({values_str})")
            filter_str = " AND ".join(conditions) if conditions else None

        # Default columns if not specified
        if columns is None:
            columns = ["chunk_id", "doc_id", "text", "module", "domain", "doc_name", "source_path"]

        # Execute search
        results = index.similarity_search(
            query_text=query_text,
            columns=columns,
            num_results=num_results,
            filters=filter_str,
            query_type=query_type
        )

        # Parse results
        documents = []
        if results and hasattr(results, 'get') and results.get('result', {}).get('data_array'):
            data_array = results['result']['data_array']

            for row in data_array:
                doc = {}
                for i, col in enumerate(columns):
                    doc[col] = row[i] if i < len(row) else None

                # Score is typically the last element
                doc["score"] = row[-1] if len(row) > len(columns) else None
                documents.append(doc)

        return documents

    def get_index_status(self) -> Dict[str, any]:
        """Get current index status."""
        index_info = self.workspace_client.vector_search_indexes.get_index(
            index_name=self.index_name
        )
        return {
            "name": index_info.name,
            "status": index_info.status,
            "index_type": index_info.index_type,
            "endpoint": index_info.endpoint_name
        }

