class XLSXLoader(DocumentLoader):
    """Load XLSX spreadsheets."""

    def load(self, file_path: Path) -> Dict[str, any]:
        wb = load_workbook(str(file_path), read_only=True)
        text = ""

        for sheet_name in wb.sheetnames:
            sheet = wb[sheet_name]
            text += f"\n\n=== Sheet: {sheet_name} ===\n\n"

            for row in sheet.iter_rows(values_only=True):
                row_text = "\t".join([str(cell) if cell is not None else "" for cell in row])
                if row_text.strip():
                    text += row_text + "\n"

        return {
            "text": text.strip(),
            "sheet_count": len(wb.sheetnames),
            "metadata": {},
            "file_hash": self.compute_hash(file_path)
        }

class TXTLoader(DocumentLoader):
    """Load plain text files."""

    def load(self, file_path: Path) -> Dict[str, any]:
        with open(file_path, "r", encoding="utf-8", errors="ignore") as f:
            text = f.read()

        return {
            "text": text.strip(),
            "line_count": text.count("\n"),
            "metadata": {},
            "file_hash": self.compute_hash(file_path)
        }

class DocumentLoaderFactory:
    """Factory for creating appropriate document loader."""

    _loaders = {
        ".pdf": PDFLoader,
        ".docx": DOCXLoader,
        ".pptx": PPTXLoader,
        ".xlsx": XLSXLoader,
        ".txt": TXTLoader,
    }

    @classmethod
    def get_loader(cls, file_path: Path) -> DocumentLoader:
        """Get appropriate loader based on file extension."""
        ext = file_path.suffix.lower()
        loader_class = cls._loaders.get(ext)

        if loader_class is None:
            raise ValueError(f"Unsupported file type: {ext}")

        return loader_class()

    @classmethod
    def load_document(cls, file_path: Path) -> Dict[str, any]:
        """Load document using appropriate loader."""
        loader = cls.get_loader(file_path)
        result = loader.load(file_path)

        # Add common metadata
        result["file_name"] = file_path.name
        result["file_path"] = str(file_path)
        result["file_size"] = file_path.stat().st_size
        result["file_type"] = file_path.suffix.lower()[1:]  # Remove dot

        return result

