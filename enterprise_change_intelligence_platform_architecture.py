"""LLM provider abstraction for multi-provider support."""

from abc import ABC, abstractmethod
from typing import Dict, List, Optional
import json
import os

class BaseLLMProvider(ABC):
    """Base class for LLM providers."""

    @abstractmethod
    def generate(
        self,
        prompt: str,
        system_prompt: Optional[str] = None,
        max_tokens: int = 4000,
        temperature: float = 0.1,
        response_format: Optional[str] = None
    ) -> str:
        """Generate text completion."""
        pass

class DatabricksLLMProvider(BaseLLMProvider):
    """Databricks Foundation Model API provider."""

    def __init__(self, model_name: str = "databricks-dbrx-instruct", endpoint: Optional[str] = None):
        self.model_name = model_name
        self.endpoint = endpoint

        # Use Databricks SDK
        from databricks.sdk import WorkspaceClient
        self.client = WorkspaceClient()

    def generate(
        self,
        prompt: str,
        system_prompt: Optional[str] = None,
        max_tokens: int = 4000,
        temperature: float = 0.1,
        response_format: Optional[str] = None
    ) -> str:
        """Generate completion using Databricks Foundation Model API."""

        # Build messages
        messages = []
        if system_prompt:
            messages.append({"role": "system", "content": system_prompt})
        messages.append({"role": "user", "content": prompt})

        # Call API
        response = self.client.serving_endpoints.query(
            name=self.endpoint or self.model_name,
            messages=messages,
            max_tokens=max_tokens,
            temperature=temperature
        )

        return response.choices[0].message.content

class OpenAILLMProvider(BaseLLMProvider):
    """OpenAI API provider."""

    def __init__(self, model_name: str = "gpt-4", api_key: Optional[str] = None):
        self.model_name = model_name
        self.api_key = api_key or os.getenv("OPENAI_API_KEY")

        try:
            import openai
            self.client = openai.OpenAI(api_key=self.api_key)
        except ImportError:
            raise ImportError("Install OpenAI: pip install openai")

    def generate(
        self,
        prompt: str,
        system_prompt: Optional[str] = None,
        max_tokens: int = 4000,
        temperature: float = 0.1,
        response_format: Optional[str] = None
    ) -> str:
        messages = []
        if system_prompt:
            messages.append({"role": "system", "content": system_prompt})
        messages.append({"role": "user", "content": prompt})

        kwargs = {
            "model": self.model_name,
            "messages": messages,
            "max_tokens": max_tokens,
            "temperature": temperature
        }

        if response_format == "json":
            kwargs["response_format"] = {"type": "json_object"}

        response = self.client.chat.completions.create(**kwargs)
        return response.choices[0].message.content

class GeminiLLMProvider(BaseLLMProvider):
    """Google Gemini API provider."""

    def __init__(self, model_name: str = "gemini-1.5-pro", api_key: Optional[str] = None):
        self.model_name = model_name
        self.api_key = api_key or os.getenv("GEMINI_API_KEY")

        try:
            import google.generativeai as genai
            genai.configure(api_key=self.api_key)
            self.model = genai.GenerativeModel(self.model_name)
        except ImportError:
            raise ImportError("Install Gemini: pip install google-generativeai")

    def generate(
        self,
        prompt: str,
        system_prompt: Optional[str] = None,
        max_tokens: int = 4000,
        temperature: float = 0.1,
        response_format: Optional[str] = None
    ) -> str:
        # Gemini combines system prompt into user prompt
        full_prompt = prompt
        if system_prompt:
            full_prompt = f"{system_prompt}\n\n{prompt}"

        response = self.model.generate_content(
            full_prompt,
            generation_config={
                "max_output_tokens": max_tokens,
                "temperature": temperature
            }
        )
        return response.text

class LLMOrchestrator:
    """High-level LLM orchestration with provider abstraction."""

    _providers = {
        "databricks": DatabricksLLMProvider,
        "openai": OpenAILLMProvider,
        "gemini": GeminiLLMProvider,
    }

    def __init__(self, provider_name: str = "databricks", **provider_kwargs):
        provider_class = self._providers.get(provider_name.lower())
        if provider_class is None:
            raise ValueError(f"Unknown provider: {provider_name}. Available: {list(self._providers.keys())}")

        self.provider = provider_class(**provider_kwargs)
        self.provider_name = provider_name

    def generate(
        self,
        prompt: str,
        system_prompt: Optional[str] = None,
        max_tokens: int = 4000,
        temperature: float = 0.1,
        response_format: Optional[str] = None
    ) -> str:
        """Generate completion using configured provider."""
        return self.provider.generate(
            prompt=prompt,
            system_prompt=system_prompt,
            max_tokens=max_tokens,
            temperature=temperature,
            response_format=response_format
        )

    def generate_json(
        self,
        prompt: str,
        system_prompt: Optional[str] = None,
        max_tokens: int = 4000,
        temperature: float = 0.1
    ) -> Dict:
        """Generate JSON-formatted response."""
        response_text = self.generate(
            prompt=prompt,
            system_prompt=system_prompt,
            max_tokens=max_tokens,
            temperature=temperature,
            response_format="json"
        )

        # Parse JSON
        try:
            return json.loads(response_text)
        except json.JSONDecodeError:
            # Fallback: extract JSON from markdown code blocks
            if "```json" in response_text:
                start = response_text.find("```json") + 7
                end = response_text.find("```", start)
                response_text = response_text[start:end].strip()
            return json.loads(response_text)

