# scripts/batch_indexing.py

import sys
sys.path.append('../src')

from pathlib import Path
from ingestion.document_loader import DocumentLoaderFactory
from ingestion.chunking_engine import ChunkingEngine
from storage.vector_store import VectorStore
import uuid
from datetime import datetime

# Configuration
REPOSITORY_PATH = Path("/dbfs/mnt/enterprise-repo")
CATALOG = "main"
SCHEMA = "change_intel"

# Initialize services
vector_store = VectorStore(
    endpoint_name="change-intel-endpoint",
    index_name=f"{CATALOG}.{SCHEMA}.document_index",
    catalog=CATALOG,
    schema=SCHEMA,
    chunks_table="document_chunks"
)

chunker = ChunkingEngine(chunk_size=800, chunk_overlap=100)

# Scan and index
for file_path in REPOSITORY_PATH.rglob("*"):
    if file_path.is_file() and file_path.suffix.lower() in [".pdf", ".docx", ".pptx", ".xlsx", ".txt"]:
        try:
            print(f"Processing: {file_path.name}")
            
            # Load document
            doc_data = DocumentLoaderFactory.load_document(file_path)
            
            # Generate chunks
            chunks = chunker.chunk_text(doc_data["text"])
            
            # Prepare rows for Delta table
            doc_id = str(uuid.uuid4())
            rows = []
            
            for i, chunk in enumerate(chunks):
                rows.append({
                    "chunk_id": f"{doc_id}_chunk_{i}",
                    "doc_id": doc_id,
                    "text": chunk,
                    "doc_name": doc_data["file_name"],
                    "module": "Auto-Detected",  # TODO: Implement auto-detection
                    "domain": "General",
                    "release": "--",
                    "doc_type": doc_data["file_type"],
                    "source_path": str(file_path),
                    "chunk_index": i,
                    "created_at": datetime.now()
                })
            
            # Write to Delta table
            from pyspark.sql import SparkSession
            spark = SparkSession.builder.getOrCreate()
            df = spark.createDataFrame(rows)
            df.write.mode("append").saveAsTable(f"{CATALOG}.{SCHEMA}.document_chunks")
            
            print(f"  ✅ Indexed {len(chunks)} chunks")
            
        except Exception as e:
            print(f"  ❌ Error: {e}")

print("\n🎉 Batch indexing complete!")
print("Run: vector_store.sync_index() to trigger Vector Search sync")
